package model;


public class Indicadores {

  private int periodo;
  private int indicador;

  public Indicadores() {
  };

  public void setPeriodo(int newVar) {
    periodo = newVar;
  }

  public int getPeriodo() {
    return periodo;
  }

  public void setIndicador(int newVar) {
    indicador = newVar;
  }

  public int getIndicador() {
    return indicador;
  }

}
